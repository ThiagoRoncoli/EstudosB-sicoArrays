public class CursoJava3 {
    
    public static void main(String[] args){

        for(int contador = 1; contador <= 100; contador++){
            System.out.println("Contador: " + contador);
        }

        int cont = 1;
        while(cont <= 100){
            System.out.println("Contador: " + cont);
            cont++;
        }
    }
}
